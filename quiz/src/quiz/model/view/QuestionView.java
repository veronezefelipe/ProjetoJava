    public QuestionView(Predefinition pred) {
        this.predefinition = pred;
        initComponents();
        
        QuestionDAO questionDAO = new QuestionDAO();
        this.questions = questionDAO.selectQuestionAleatorias(predefinition.getQtdQuestion(), predefinition.getTheme(), predefinition.getDifficult());
        
        this.question = this.questions.get(c);
        
        carregaQuestao();
        
        
    }
    
    private void checkFinal(){
        if(predefinition.getQtdQuestion() > c){
            this.question = this.questions.get(c++);
        }else{
            this.dispose();
            name = JOptionPane.showInputDialog("VocÃª acertou: " + score + " de " + predefinition.getQtdQuestion() + ", informe seu nome:");
            rankingDAO.adcRanking(name, score);                                
        }
    }
    
    public void carregaQuestao(){
        jLbQuestion.setText(question.getQuestion());
        jButton1.setText(question.getAlternative1());
        jButton2.setText(question.getAlternative2());
        jButton3.setText(question.getAlternative3());
        jButton4.setText(question.getAlternative4());
        System.out.println(question.getRightAnswer());
    }

